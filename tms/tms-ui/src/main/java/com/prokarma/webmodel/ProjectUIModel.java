package com.prokarma.webmodel;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

public class ProjectUIModel implements Serializable {

	private static final long serialVersionUID = -7711076461333043123L;
	
	private String projectName;
	private String codename;
	private String techUsed;
	private String description;
	private Date start_Date;
	private Date end_Date;
	private String status;
	private int managerId;
	private List<String> assignedEmployees;
	private List<String> otherEmployees;


	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getCodename() {
		return codename;
	}

	public void setCodename(String codename) {
		this.codename = codename;
	}

	public String getTechUsed() {
		return techUsed;
	}

	public void setTechUsed(String techUsed) {
		this.techUsed = techUsed;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Date getStart_Date() {
		return start_Date;
	}

	public void setStart_Date(Date start_Date) {
		this.start_Date = start_Date;
	}

	public Date getEnd_Date() {
		return end_Date;
	}

	public void setEnd_Date(Date end_Date) {
		this.end_Date = end_Date;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public List<String> getAssignedEmployees() {
		return assignedEmployees;
	}

	public void setAssignedEmployees(List<String> assignedEmployees) {
		this.assignedEmployees = assignedEmployees;
	}

	public int getManagerId() {
		return managerId;
	}

	public void setManagerId(int managerId) {
		this.managerId = managerId;
	}

	public List<String> getOtherEmployees() {
		return otherEmployees;
	}

	public void setOtherEmployees(List<String> otherEmployees) {
		this.otherEmployees = otherEmployees;
	}


}
